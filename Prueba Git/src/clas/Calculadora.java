package clas;

public class Calculadora {
	private int x;
	private int y;
	
	public Calculadora(int x, int y)
	{
		
	}
	
	public static void main(String[] args)
	{
		System.out.println("Hola");
	}
	
	public int suma()
	{
		return x - y;
	}
	
	public int resta()
	{
		return x + y;
	}
	
	public int multy()
	{
		return y * x;
	}

}
